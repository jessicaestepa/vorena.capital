#!/usr/bin/env node
/**
 * Writes js/supabase-config.js for deploy (Vercel env) or local dev.
 * Env: VORENA_SUPABASE_URL, VORENA_SUPABASE_ANON_KEY
 */
const fs = require('fs');
const path = require('path');

const outPath = path.join(__dirname, '..', 'js', 'supabase-config.js');
const url =
  process.env.VORENA_SUPABASE_URL ||
  'https://vebkqcijfauvmtrblbeq.supabase.co';
const anonKey = process.env.VORENA_SUPABASE_ANON_KEY || '';
const table = process.env.VORENA_SUPABASE_TABLE || 'contact_submissions';

if (!anonKey) {
  console.warn(
    'generate-supabase-config: VORENA_SUPABASE_ANON_KEY not set; forms will show a config error on the site.'
  );
}

const contents = `/**
 * Generated by scripts/generate-supabase-config.js — do not commit secrets.
 * Dashboard → Settings → API → sb_publishable_...
 */
window.VORENA_SUPABASE = {
  url: ${JSON.stringify(url.replace(/\/$/, ''))},
  anonKey: ${JSON.stringify(anonKey)},
  table: ${JSON.stringify(table)}
};
`;

fs.writeFileSync(outPath, contents, 'utf8');
console.log('Wrote', outPath);
